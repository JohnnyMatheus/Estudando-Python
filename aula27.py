'''
Fatiamento de Strings

fatiamento[i:f:p] [::]
inicio e fim

'''
variavel = 'Johnny Matheus'
print(len(variavel))
print(variavel[4:8])
print(variavel[4:])
print(variavel[:4])
print(variavel[0:len(variavel):1]) #passo de 1 em 1
print(variavel[0:len(variavel):2]) #passo de 2 em 2
print(variavel[-1:-15:-1])