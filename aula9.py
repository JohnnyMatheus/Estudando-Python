#OPERADORES ARITMÉTICOS

#ADIÇÃO
adicao = 2+2
print('Adição:',adicao)

#SUBTRAÇÃO
subtracao = 2-2
print('Subtração:',subtracao)

#MULTIPLICAÇÃO
multiplicacao = 20*20
print('Multiplicação:',multiplicacao)

#DIVISÃO
divisao = 20/5
print('Divisão:',divisao)

#DIVISÃO INTEIRA
divisao_inteira = 20//5
print('Divisão Inteira:',divisao_inteira)

#EXPONENCIAÇÃO
exponenciacao = 20**2
print('Exponenciação:',exponenciacao)

#MÓDULO - RESTO DA DIVISÃO
modulo = 20%5
print('Módulo:',modulo)